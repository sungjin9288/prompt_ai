    "drop policy if exists feedback_write_member on public.feedback;",
    "create policy feedback_write_member on public.feedback",
    "  for all",
    "  using (exists (",
    "    select 1 from public.prompt_assets asset",
    "    where asset.id = feedback.prompt_asset_id",
